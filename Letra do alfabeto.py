
letra = input("Digite uma letra do alfabeto: ")
letra = letra.lower()

if letra == 'a':
    print("Avião")
elif letra == 'b':
    print("Bola")
elif letra == 'c':
    print("Carro")
elif letra == 'd':
    print("Dado")
elif letra == 'e':
    print("Elefante")
elif letra == 'f':
    print("Foca")
elif letra == 'g':
    print("Gato")
elif letra == 'h':
    print("Hipopótamo")
elif letra == 'i':
    print("Igreja")
elif letra == 'J':
    print("Janela")
elif letra == 'u':
    print("Uva")
elif letra == 'k':
    print("Kalunga")
else:
    print("Não conheço uma palavra que comece com essa letra.")
